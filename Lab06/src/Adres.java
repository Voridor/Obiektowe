public class Adres {
    String miejscowość;
    String ulica;
    int nrDomu;
    String kodPocztowy;

    public Adres(String miejscowość, String ulica, int nrDomu, String kodPocztowy) {
        this.miejscowość = miejscowość;
        this.ulica = ulica;
        this.nrDomu = nrDomu;
        this.kodPocztowy = kodPocztowy;
    }
}
